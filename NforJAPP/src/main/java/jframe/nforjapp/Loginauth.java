    private boolean authenticateUser(String enteredUsername, String enteredPassword) {
    List<Tradeloginusers> tradeloginusers = new LoginEntilysql().getlists();
    List<Tradeloginusers> tradeloginarry = new ArrayList<>();                     
                          tradeloginarry.add(new Tradeloginusers(2,"pass","pass","pass","pass"));
    //with this i can use the data from the database to loginn
    for (Tradeloginusers user : tradeloginusers) {
        System.out.println(user.displaytradeloginusers());
        if (user.getUsername().equals(enteredUsername) && user.getPasswords().equals(enteredPassword)) {
            return true; // Authentication successful
        }
    }
    //With this i can use the data from the arraylist to loginn
    for (Tradeloginusers user : tradeloginarry) {
        System.out.println(user.displaytradeloginusers());
        if (user.getUsername().equals(enteredUsername) && user.getPasswords().equals(enteredPassword)) {
            return true; // Authentication successful
        }
    }

    return false; // Authentication failed
    }
    








