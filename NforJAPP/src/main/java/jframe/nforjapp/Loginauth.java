    String enteredUsername = jTextField1.getText();
    String enteredPassword = jTextField2.getText();

    // Authenticate user
    boolean authenticated = authenticateUser(enteredUsername, enteredPassword);

    // Display result
    if (authenticated) {
        JOptionPane.showMessageDialog(this, "Login successful");

        // Close the current login window
        this.dispose();

        // Open the welcome window
        SwingUtilities.invokeLater(() -> {
            Loginwelcome welcomeFrame = new Loginwelcome();
            welcomeFrame.setVisible(true);
        });
    } else {
        JOptionPane.showMessageDialog(this, "Authentication failed");
        System.out.println("Entered Username: " + enteredUsername);
        System.out.println("Entered Password: " + enteredPassword);
    }
    
